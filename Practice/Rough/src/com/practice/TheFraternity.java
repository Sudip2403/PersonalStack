package com.practice;

public class TheFraternity {

	public static void main(String[] args) {
		String[] a = { "|-|-|-|-|-|-|-|-|-|-|-|-|-|-|-|-", "-|-|-|-|-|-|-|-|-|-|-|-|-|-|-|-|",
				"|-|-|-|-|-|-|-|-|-|-|-|---|-|-|-", "-|-|-|-|-|-|||-|-|-|-|-|-|---|-|",
				"|-|-|-|-|-|-|-|-|-|-|-|-|-|-|-|-", "-|-|-|-|-|-|-|-|-|-|-|-|-|-|-|-|",
				"|-|-|---|-|-|-|-|-|---|-|-|-|-|-", "-|-|-|-|-|-|-|-|-|-|-|-|-|-|-|-|",
				"|-|-|-|-|-|-|-|-|-|-|-|-|-|-|-|-", "-|-|-|-|-|-|-|-|-|-|-|-|-|-|-|-|",
				"|-|---|-|-|-|-|-|-|-|-|-|-|-|-|-", "-|-|-|-|-|-|-|-|-|-|-|-|-|-|-|-|",
				"|-|-|-|-|||-|-|-|-|-|-|-|-|-|-|-", "-|-|-|-|-|-|-|-|||-|-|-|-|-|-|-|",
				"|-|-|-|-|-|-|-|-|-|-|-|-|---|-|-", "-|-|-|-|-|-|-|-|-|-|-|||-|-|-|-|",
				"|-|-|-|-|---|-|-|-|-|-|-|-|-|-|-", "-|-|-|-|-|-|-|-|-|-|-|-|-|-|-|-|",
				"|-|-|-|-|-|-|-|-|-|-|-|-|-|-|-|-", "-|-|-|-|-|-|-|---|-|-|-|-|-|-|-|",
				"|-|-|-|-|-|-|-|-|-|-|-|-|-|-|-|-", "-|-|-|-|-|-|-|-|-|---|-|-|-|-|-|",
				"|-|||-|-|-|-|-|-|-|-|-|-|-|-|-|-", "-|-|-|-|-|-|-|-|-|-|-|-|-|-|-|-|",
				"|-|-|-|-|||-|---|-|-|-|-|-|-|-|-", "-|-|-|-|-|-|-|-|-|-|-|-|-|-|-|-|",
				"|-|-|-|-|-|-|-|-|-|-|-|-|-|-|-|-", "-|-|-|-|-|-|-|-|-|-|-|-|-|-|-|-|",
				"|-|-|-|-|-|-|-|-|-|-|-|-|-|-|-|-", "-|-|-|-|-|-|-|-|-|-|-|-|-|-|-|-|",
				"|-|-|-|-|-|-|-|-|-|-|-|-|-|-|-|-", "-|-|-|-|-|-|-|-|-|-|-|-|-|-|-|-|" };
		System.out.println(new TheFraternity().theFraternity(a));
	}

	String theFraternity(String[] a) {
		int n = a.length, m = a[0].length(), q = 0, e = 0;
		String r = "", p = "";
		for (int i = 0; i < n; i++) {
			if (q == 0)
				e = 0;
			else
				e = 1;
			for (int j = 0; j < m; j++) {
				if (p.length() == 8) {
					r += (char) Integer.parseInt(p, 2);
					p = "";
				}
				if (e == 0) {
					if (a[i].charAt(j) == '|')
						p += '1';
					e = 1;
				} else {
					if (a[i].charAt(j) == '-')
						p += '0';
					e = 0;
				}
			}
			if (q == 0)
				q = 1;
			else
				q = 0;
		}
		if (p.length() == 8)
			r += (char) Integer.parseInt(p, 2);
		return r;
	}

}
